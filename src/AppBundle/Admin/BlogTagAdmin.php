<?php

namespace AppBundle\Admin;

use Sonata\AdminBundle\Datagrid\ListMapper;
use Sonata\AdminBundle\Datagrid\DatagridMapper;
use Sonata\AdminBundle\Form\FormMapper;

/**
 * Class BlogTagAdmin
 *
 * @category Admin
 * @package  AppBundle\Admin
 * @author   David RomanÃ­ <david@flux.cat>
 */
class BlogTagAdmin extends BaseAdmin
{
    protected $classnameLabel = 'Etiqueta';
    protected $baseRoutePattern = 'blog/etiqueta';
    protected $datagridValues = array('_sort_by' => 'name');

    /**
     * Configure list view
     *
     * @param ListMapper $mapper
     */
    protected function configureListFields(ListMapper $mapper)
    {
        unset($this->listModes['mosaic']);
        $mapper
            ->add(
                'name',
                null,
                array(
                    'label'    => 'Nom',
                    'editable' => true,
                )
            )
//            ->add(
//                'posts',
//                null,
//                array(
//                    'label'    => 'Articles',
//                    'editable' => false,
//                )
//            )
            ->add(
                'enabled',
                null,
                array(
                    'label'    => 'Actiu',
                    'editable' => true,
                )
            )
            ->add(
                '_action',
                'actions',
                array(
                    'actions' => array(
                        'edit'   => array('template' => '::Admin/Buttons/list__action_edit_button.html.twig'),
                        'delete' => array('template' => '::Admin/Buttons/list__action_delete_button.html.twig'),
                    ),
                    'label'   => 'Accions',
                )
            );
    }

    /**
     * Configure list view filters
     *
     * @param DatagridMapper $datagrid
     */
    protected function configureDatagridFilters(DatagridMapper $datagrid)
    {
        $datagrid
            ->add(
                'name',
                null,
                array(
                    'label' => 'Nom',
                )
            )
            ->add(
                'posts',
                null,
                array(
                    'label' => 'Articles',
                )
            )
            ->add(
                'enabled',
                null,
                array(
                    'label' => 'Actiu',
                )
            );
    }

    /**
     * Configure edit view
     *
     * @param FormMapper $formMapper
     */
    protected function configureFormFields(FormMapper $formMapper)
    {
        $formMapper
            ->with('Etiqueta', array('class' => 'col-md-7'))
            ->add(
                'name',
                null,
                array(
                    'label' => 'Nom',
                )
            )
            ->end()
            ->with('Controls', array('class' => 'col-md-5'))
            ->add(
                'enabled',
                null,
                array(
                    'label'    => 'Actiu',
                    'required' => false,
                )
            )
            ->end()
            ->with('Articles', array('class' => 'col-md-12'))
            ->add(
                'posts',
                null,
                array(
                    'label'    => 'Articles',
                    'disabled' => true,
                )
            )
            ->end();
    }
}
